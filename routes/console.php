Artisan::command('subscription:audit-tenant-invoices {--tenant=} {--cleanup-shadow}', function () {
    if (tenancy()->initialized) {
        tenancy()->end();
    }

    /** @var TenantSubscriptionInvoiceService $invoiceService */
    $invoiceService = app(TenantSubscriptionInvoiceService::class);
    $tenantId = trim((string) $this->option('tenant'));
    $cleanupShadow = (bool) $this->option('cleanup-shadow');
    $report = $invoiceService->auditTenants($tenantId !== '' ? $tenantId : null);

    $this->info(sprintf(
        'Audit invoice tenant selesai. total=%d, relational_only=%d, relational_shadow=%d, legacy_only=%d, mismatch=%d, empty=%d',
        (int) data_get($report, 'counts.tenants', 0),
        (int) data_get($report, 'counts.relational_only', 0),
        (int) data_get($report, 'counts.relational_shadow', 0),
        (int) data_get($report, 'counts.legacy_only', 0),
        (int) data_get($report, 'counts.mismatch', 0),
        (int) data_get($report, 'counts.empty', 0)
    ));

    if (! (bool) data_get($report, 'tables_exist', false)) {
        $this->warn('Schema relasional invoice belum ada. Semua tenant saat ini masih fallback ke legacy.');
    }

    $cleanedCount = 0;

    foreach ((array) data_get($report, 'rows', []) as $row) {
        $line = sprintf(
            '%s | %s | rel=%d | legacy=%d | cleanup=%s',
            (string) ($row['tenant_id'] ?? '-'),
            (string) ($row['label'] ?? '-'),
            (int) ($row['relational_count'] ?? 0),
            (int) ($row['legacy_count'] ?? 0),
            (bool) ($row['cleanup_ready'] ?? false) ? 'ready' : 'no'
        );

        $legacyMetaKeys = collect((array) ($row['legacy_meta_keys'] ?? []))->implode(', ');
        if ($legacyMetaKeys !== '') {
            $line .= ' | meta=' . $legacyMetaKeys;
        }

        $mismatchInvoices = collect((array) ($row['mismatch_invoice_numbers'] ?? []))->take(5)->implode(', ');
        if ($mismatchInvoices !== '') {
            $line .= ' | mismatch=' . $mismatchInvoices;
        }

        $this->line($line);

        if (! $cleanupShadow || ! (bool) ($row['cleanup_ready'] ?? false)) {
            continue;
        }

        $tenant = Tenant::query()->find((string) ($row['tenant_id'] ?? ''));

        if (! $tenant) {
            continue;
        }

        $cleanup = $invoiceService->cleanupLegacyShadow($tenant);

        if ((bool) ($cleanup['cleaned'] ?? false)) {
            $cleanedCount++;
        }
    }

    if ($cleanupShadow) {
        $this->info(sprintf('Cleanup shadow legacy selesai. %d tenant dibersihkan.', $cleanedCount));
    }
})->purpose('Audit health invoice tenant legacy vs relasional, opsional cleanup shadow legacy yang sudah aman');

Artisan::command('tirta:audit-disconnections {--tenant=}', function () {
    if (tenancy()->initialized) {
        tenancy()->end();
    }

    $tenantId = trim((string) $this->option('tenant'));

    $query = Tenant::query()
        ->when($tenantId !== '', fn ($q) => $q->whereKey($tenantId))
        ->where('saas_type', 'tirta');

    $tenants = $query->get();

    if ($tenants->isEmpty()) {
        $this->comment('Tidak ada tenant Tirta yang cocok.');

        return;
    }

    /** @var TirtaConnectionLifecycleService $lifecycle */
    $lifecycle = app(TirtaConnectionLifecycleService::class);
    $totalDisconnected = 0;
    $totalScanned = 0;

    foreach ($tenants as $tenant) {
        tenancy()->initialize($tenant);

        /** @var \Modules\BaseFeature\Models\TenantSetting $setting */
        $setting = \Modules\BaseFeature\Models\TenantSetting::query()->firstOrCreate(
            [],
            [
                'brand_name' => (string) ($tenant->name ?? $tenant->id ?? config('app.name')),
                'description' => 'Workspace Tirta belum dikustomisasi.',
                'theme_color' => '#0891b2',
            ]
        );

        $report = $lifecycle->auditAndDisconnectOverdueConnections($setting, now());
        $totalDisconnected += (int) ($report['disconnected'] ?? 0);
        $totalScanned += (int) ($report['scanned'] ?? 0);

        $this->line(sprintf(
            '%s | scanned=%d | disconnected=%d | threshold=%d months',
            (string) $tenant->getKey(),
            (int) ($report['scanned'] ?? 0),
            (int) ($report['disconnected'] ?? 0),
            (int) ($report['threshold_months'] ?? 3)
        ));

        tenancy()->end();
    }

    $this->info(sprintf('Audit cabut Tirta selesai. total_scanned=%d total_disconnected=%d', $totalScanned, $totalDisconnected));
})->purpose('Audit sambungan Tirta yang menunggak dan otomatis cabut setelah ambang bulan');

Artisan::command('subscription:repair-tenant-invoices {--tenant=} {--mode=auto} {--cleanup-shadow}', function () {
    if (tenancy()->initialized) {
        tenancy()->end();
    }

    /** @var TenantSubscriptionInvoiceService $invoiceService */
    $invoiceService = app(TenantSubscriptionInvoiceService::class);
    $tenantId = trim((string) $this->option('tenant'));
    $mode = trim((string) $this->option('mode'));
    $cleanupShadow = (bool) $this->option('cleanup-shadow');
    $query = Tenant::query()->orderBy('id');

    if ($tenantId !== '') {
        $query->whereKey($tenantId);
    }

    $tenants = $query->get();

    if ($tenants->isEmpty()) {
        $this->warn('Tenant target tidak ditemukan untuk repair invoice.');

        return;
    }

    $resultCounts = [
        'synced' => 0,
        'cleaned' => 0,
        'skipped' => 0,
    ];

    foreach ($tenants as $tenant) {
        $result = $invoiceService->repairTenantInvoices($tenant, $mode, $cleanupShadow);
        $action = (string) ($result['action'] ?? 'skipped');

        if (str_contains($action, 'synced')) {
            $resultCounts['synced']++;
        }

        if (str_contains($action, 'cleaned')) {
            $resultCounts['cleaned']++;
        }

        if ($action === 'skipped') {
            $resultCounts['skipped']++;
        }

        $this->line(sprintf(
            '%s | %s -> %s | action=%s | reason=%s',
            (string) ($result['tenant_id'] ?? $tenant->getKey()),
            (string) data_get($result, 'before.label', '-'),
            (string) data_get($result, 'after.label', '-'),
            $action,
            (string) ($result['reason'] ?? '-')
        ));
    }

    $this->info(sprintf(
        'Repair invoice tenant selesai. synced=%d, cleaned=%d, skipped=%d',
        $resultCounts['synced'],
        $resultCounts['cleaned'],
        $resultCounts['skipped']
    ));
})->purpose('Repair atau backfill invoice tenant antar layer legacy dan relasional dengan mode aman');

