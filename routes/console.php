    $moduleService->syncFromLegacyCatalog(CentralSetting::legacyModuleCatalog());
    $packageService->syncFromLegacySettings($platform);

    $this->info('Sync katalog modul dan package ke tabel relasional selesai.');
})->purpose('Sync legacy module/package catalog ke tabel relasional subscription-aware');

