Artisan::command('subscription:sync-relational-catalogs {--platform=} ', function () {
    if (tenancy()->initialized) {
        tenancy()->end();
    }

    $platform = $this->option('platform');
    $platform = is_string($platform) && in_array($platform, CentralSetting::availablePlatformTypes(), true)
        ? $platform
        : null;

    /** @var PlatformModuleCatalogService $moduleService */
    $moduleService = app(PlatformModuleCatalogService::class);
    /** @var SubscriptionPackageCatalogService $packageService */
    $packageService = app(SubscriptionPackageCatalogService::class);

    if (! $moduleService->tablesExist() || ! $packageService->tablesExist()) {
        $this->error('Tabel relasional subscription belum ada. Jalankan migrasi central dulu.');

        return;
